console.log('\n-------------------------------------------------- 01');
(function() {
  /*
   * ADT（Abstract Data Type）抽象数据类型，指的是某个数据类型的数学模型，
   * 即，通过一组数据（values）和操作（operations）来描述一类对象的逻辑行为，
   * ADT 描述了对象应该具有的行为，但是没有指定具体的实现方式，因此称之为抽象地
   */
  /*
   * List ADT 就是对 list 这种数据类型建立的数学模型，
   * 即，一个 list 对象，它应该具有哪些行为？哪些操作？
   * 一份完整的 list ADT 如下：
   *
   * listSize       属性，list 的元素个数
   * pos            属性，list 中的当前位置
   * length         属性，返回 list 的元素个数
   * clear()        方法，清空 list，移除所有元素
   * toString()     方法，返回 list 的字符串形式
   * getElement()   方法，返回 list 中当前位置的元素
   * insert()       方法，往指定的元素后面，插入新的元素
   * append()       方法，往 list 的最后面添加元素
   * remove()       方法，删除指定位置的元素
   * front()        方法，将当前位置设置为第一个元素
   * end()          方法，将当前位置设置为最后一个元素
   * prev()         方法，将当前位置往前移动一位
   * next()         方法，将当前位置往后移动一位
   * currPos()      方法，返回当前位置
   * moveTo()       方法，将当前位置移动到指定的位置
   */
})();
